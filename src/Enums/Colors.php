    private static array|null $cache = null;

    public static function toArray(): array
    {
        if (static::$cache === null) {
            static::$cache = [];
        }

        $calledClass = get_called_class();
        if (!array_key_exists($calledClass, static::$cache)) {
            $reflect = new ReflectionClass($calledClass);
            static::$cache[$calledClass] = $reflect->getConstants();
        }

        return static::$cache[$calledClass];
    }

    public static function isValidName($name, $strict = false): bool
    {
        $constants = static::toArray();

        if ($strict) {
            return array_key_exists($name, $constants);
        }

        $keys = array_map('strtolower', array_keys($constants));
        return in_array(strtolower($name), $keys);
    }

    public static function isValidValue($value, $strict = true): bool
    {
        $values = array_values(static::toArray());
        return in_array($value, $values, $strict);
    }

